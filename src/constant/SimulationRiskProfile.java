package constant;

/**
 * Enum represents the risk levels that the simulation can utilize
 *
 * Created by L. Arroyo on 9/11/2019
 */

public enum SimulationRiskProfile
{
    // VALUES
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    LOW(1),
    MODERATE(4),
    AGGRESSIVE(6);

    // FIELDS
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    private final int maxNullCount;

    // CONSTRUCTORS
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    SimulationRiskProfile(final int maxNullCount)
    {
        this.maxNullCount = maxNullCount;
    }

    // ACCESS METHODS
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public int getMaxNullCount() {
        return maxNullCount;
    }
}
